; =============================================================================
; moz-os phase 0002: stage-2 loader placeholder
; =============================================================================

format binary
use16
org 0x8000

start:
    ; -------------------------------------------------------------------------
    ; 1. initialize segments
    ; -------------------------------------------------------------------------
    cli
    xor ax, ax
    mov ds, ax
    mov es, ax
    mov ss, ax
    mov sp, 0x8000      ; stack grows down from 0x8000 (below stage 2)
    sti

    ; -------------------------------------------------------------------------
    ; 2. print banner
    ; -------------------------------------------------------------------------
    mov si, banner_msg
    call print_string

    ; -------------------------------------------------------------------------
    ; 3. halt
    ; -------------------------------------------------------------------------
halt_loop:
    hlt
    jmp halt_loop

; =============================================================================
; routine: print_string
; input: si = pointer to null-terminated string
; =============================================================================
print_string:
    push ax
    push si
.next_char:
    lodsb
    test al, al
    jz .done
    mov ah, 0x0e
    int 0x10
    jmp .next_char
.done:
    pop si
    pop ax
    ret

; =============================================================================
; data
; =============================================================================
banner_msg db 'stage2: moz-os loader skeleton', 13, 10, 0
